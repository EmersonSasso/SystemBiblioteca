    private void jcbTituloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcbTituloActionPerformed
        Limpa();
        btnSalvar.setEnabled(jcbTitulo.getSelectedIndex() != 0);
        jcbGenero.setEnabled(jcbTitulo.getSelectedIndex() != 0);
        if (jcbTitulo.getSelectedIndex() != 0) {
            cbAtivo.setSelected(Constantes.getLlivro().get(jcbTitulo.getSelectedIndex() - 1).getAtivo());
            jftISBN.setText(Constantes.getLlivro().get(jcbTitulo.getSelectedIndex() - 1).getIsbn().toString());
            jtAutor.setText(Constantes.getLlivro().get(jcbTitulo.getSelectedIndex() - 1).getAutor());
            jtEditora.setText(Constantes.getLlivro().get(jcbTitulo.getSelectedIndex() - 1).getEditora());
            jtTitulo.setText(Constantes.getLlivro().get(jcbTitulo.getSelectedIndex() - 1).getLivro());
            jfData.setText(new SimpleDateFormat("dd-MM-yyyy").format(Constantes.getLlivro().get(jcbTitulo.getSelectedIndex() - 1).getPublicado()));
            jcbGenero.setSelectedIndex(Constantes.getMet().checkGenero(Constantes.getLlivro().get(jcbTitulo.getSelectedIndex() - 1).getGenero()));
        }
    }//GEN-LAST:event_jcbTituloActionPerformed

