    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        text = "";
        if (jcbGenero.getSelectedIndex() == 0 && "EDITAR".equals(btnSalvar.getText())) {
            text = text + "- Por favor escolher um genero!\n";
        }
        if (jftISBN.getText().length() != 13) {
            text = text + "- DDigite um ISBN de exatos 13 digitos!\n";
        }
        if ("".equals(text)) {
            try {
                if ("EDITAR".equals(btnSalvar.getText())) {
                    Constantes.getLlivro().get(jcbTitulo.getSelectedIndex() - 1).setAtivo(cbAtivo.isSelected());
                    resultado = Constantes.getMl().doLivro(Constantes.getLlivro().get(jcbTitulo.getSelectedIndex() - 1), Constantes.getLgenero().get(jcbGenero.getSelectedIndex() - 1).getGeneroId().toString(), false);
                } else {
                    Constantes.setVwlivro(new VwLivro());
                    Constantes.getVwlivro().setLivroId(BigInteger.valueOf(0));
                    Constantes.getVwlivro().setIsbn(BigInteger.valueOf(Long.valueOf(jftISBN.getText())));
                    Constantes.getVwlivro().setLivro(jtTitulo.getText());
                    Constantes.getVwlivro().setEditora(jtEditora.getText());
                    Constantes.getVwlivro().setAutor(jtAutor.getText());
                    Constantes.getVwlivro().setAtivo(cbAtivo.isSelected());
                    String aux[] = jfData.getText().split("-");
                    Constantes.getVwlivro().setPublicado(java.sql.Date.valueOf(aux[2] + "-" + aux[1] + "-" + aux[0]));

                    resultado = Constantes.getMl().doLivro(Constantes.getVwlivro(), Constantes.getLgenero().get(jcbGenero.getSelectedIndex() - 1).getGeneroId().toString(), false);
                }
                if (btnSalvar.getText().equals("EDITAR")) {
                    if (resultado == 0) {
                        text = "Problema ao realizar a edição de " + Constantes.getLlivro().get(jcbTitulo.getSelectedIndex() - 1).getLivro().toUpperCase();
                    } else {
                        text = Constantes.getLlivro().get(jcbTitulo.getSelectedIndex() - 1).getLivro().toUpperCase() + " editado com sucesso!";
                        Limpa();
                    }
                } else {
                    if (resultado == 0) {
                        text = "Problema ao realizar o cadastro de " + Constantes.getVwlivro().getLivro().toUpperCase();
                    } else {
                        text = Constantes.getVwlivro().getLivro().toUpperCase() + " Cadastrado com sucesso!";
                        Limpa();
                    }
                }

            } catch (SQLException ex) {
                Logger.getLogger(Form_Livro.class.getName()).log(Level.SEVERE, null, ex);
                text = "Erro ao " + btnSalvar.getText().toLowerCase() + "!";
            }
        } else {
            text = "Por favor se atentar ao seguinte: \n\n" + text;
        }
        JOptionPane.showMessageDialog(this, text);
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        if ("".equals(jftISBN.getText()) || (jftISBN.getText().length() != 13)) {
            JOptionPane.showMessageDialog(Constantes.getMain(), "Preencha o campo ISBN corretamente (Tam. 13)");
        } else {
            if (Constantes.getMet().CallApi(jftISBN.getText())) {
                jtAutor.setText(Constantes.getVwlivro().getAutor());
                jtEditora.setText(Constantes.getVwlivro().getEditora());
                jtTitulo.setText(Constantes.getVwlivro().getLivro());
                jfData.setText(new SimpleDateFormat("dd-MM-yyyy").format(Constantes.getVwlivro().getPublicado()));
                jcbGenero.setEnabled(true);
            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

